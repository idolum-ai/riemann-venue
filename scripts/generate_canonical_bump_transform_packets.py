    scaled_radius = required_radius * EXP_CACHE_DENOMINATOR
    radius_numerator = (
        scaled_radius.numerator + scaled_radius.denominator - 1
    ) // scaled_radius.denominator
    return center, Fraction(radius_numerator, EXP_CACHE_DENOMINATOR)


