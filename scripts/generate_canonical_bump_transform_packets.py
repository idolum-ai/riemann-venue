def paired_packet_blocks(
    packet: Packet,
) -> list[tuple[Fraction, Fraction, Fraction]]:
    cells = paired_packet_cell_data(packet)
    return [
        tuple(
            sum(
                cells[index][coordinate]
                for index in range(start, start + PAIRED_PACKET_BLOCK_SIZE)
            )
            for coordinate in range(3)
        )
        for start in range(0, 64, PAIRED_PACKET_BLOCK_SIZE)
    ]


