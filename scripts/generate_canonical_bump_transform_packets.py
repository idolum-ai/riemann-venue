def polynomial_value(coefficients: list[int], x: float) -> float:
    value = 0.0
    for coefficient in reversed(coefficients):
        value = value * x + coefficient
    return value


def canonical_bump_jet(order: int, x: float) -> float:
    gap = 1.0 - x * x
    bump = math.exp(1.0 - 1.0 / gap)
    return (
        polynomial_value(BUMP_JET_POLYNOMIALS[order], x)
        * bump
        / gap ** (2 * order)
    )


def raw_jet(order: int, re: Fraction, im: Fraction, q: Fraction) -> complex:
    """Numerically mirror the canonical raw-integrand Leibniz jet."""
    z = complex(float(re), float(im))
    x = float(q)
    kernel = cmath.exp(1j * z * x)
    lam = 1j * z
    return sum(
        math.comb(order, bump_order)
        * canonical_bump_jet(bump_order, x)
        * lam ** (order - bump_order)
        * kernel
        for bump_order in range(order + 1)
    )


