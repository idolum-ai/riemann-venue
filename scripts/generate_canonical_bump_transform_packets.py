def render_paired_packet(packet: Packet) -> str:
    """Assemble exact block shards into the certified packet aggregate."""
    blocks = paired_packet_blocks(packet)
    center_re, center_im, error = tuple(
        sum(data[coordinate] for data in blocks) for coordinate in range(3)
    )
    declaration = f"canonicalBump{packet.stem}Paired"

    lines: list[str] = []
    for block in range(PAIRED_PACKET_BLOCK_COUNT):
        lines.append(
            f"import RiemannVenue.Venue.{paired_packet_block_stem(packet, block)}"
        )
    lines.extend([
        "",
        *header(
            "scripts/generate_canonical_bump_transform_packets.py",
            f"Paired canonical-bump transform packet: {packet.stem}",
        ),
        "namespace RiemannVenue.Venue",
        "",
        "noncomputable section",
        "",
        f"theorem {declaration}BlockData_eq (block : Fin {PAIRED_PACKET_BLOCK_COUNT}) :",
        "    canonicalBumpPairedPositiveBlockDataQ",
        f"      canonicalBump{packet.stem}Real {declaration}Recurrences block =",
        f"      {declaration}ExactBlocks block := by",
        "  fin_cases block",
    ])
    for block in range(PAIRED_PACKET_BLOCK_COUNT):
        lines.append(
            f"  simpa [{declaration}ExactBlocks] using "
            f"{declaration}Block{block:02d}Data_eq"
        )
    lines.extend([
        "",
        f"theorem {declaration}CenterQ_eq :",
        "    canonicalBumpPairedPositivePacketCenterQ",
        f"      canonicalBump{packet.stem}Real {declaration}Recurrences =",
        f"      ({lean_rat(center_re)}, {lean_rat(center_im)}) := by",
        "  rw [canonicalBumpPairedPositivePacketCenterQ_eq_blocks]",
        f"  simp_rw [{declaration}BlockData_eq]",
        "  decide +kernel",
        "",
        f"theorem {declaration}ErrorQ_eq :",
        "    canonicalBumpPairedPositivePacketErrorQ",
        f"      canonicalBump{packet.stem}Real {declaration}Recurrences =",
        f"      {lean_rat(error)} := by",
        "  rw [canonicalBumpPairedPositivePacketErrorQ_eq_blocks]",
        f"  simp_rw [{declaration}BlockData_eq]",
        "  decide +kernel",
        "",
        f"theorem {declaration}Packet_center :",
        f"    {declaration}Packet.center =",
        f"      (({lean_rat(center_re)} : ℚ) : ℝ) +",
        f"        (({lean_rat(center_im)} : ℚ) : ℝ) * Complex.I := by",
        f"  rw [{declaration}Packet,",
        "    canonicalBumpPairedPositivePacket_center,",
        f"    {declaration}CenterQ_eq]",
        "",
        f"theorem {declaration}Packet_error :",
        f"    {declaration}Packet.error = (({lean_rat(error)} : ℚ) : ℝ) := by",
        f"  rw [{declaration}Packet,",
        "    canonicalBumpPairedPositivePacket_error,",
        f"    {declaration}ErrorQ_eq]",
        "",
        "end",
        "",
        "end RiemannVenue.Venue",
        "",
    ])
    return "\n".join(lines)


def generated_files() -> dict[Path, str]:
    files = {
        VENUE / "BoundaryCanonicalBumpTransformPacketGrid.lean": render_grid(),
    }
    for packet in PACKETS:
        files[
            VENUE / f"BoundaryCanonicalBumpTransformPacket{packet.stem}.lean"
        ] = render_packet(packet)
        for reflected in (False, True):
            files[VENUE / f"{kernel_recurrence_stem(packet, reflected)}.lean"] = (
                render_kernel_recurrence(packet, reflected)
            )
        files[VENUE / f"{paired_packet_stem(packet)}.lean"] = (
            render_paired_packet(packet)
        )
        files[VENUE / f"{paired_packet_data_stem(packet)}.lean"] = (
            render_paired_packet_data(packet)
        )
        for block in range(PAIRED_PACKET_BLOCK_COUNT):
            files[VENUE / f"{paired_packet_block_stem(packet, block)}.lean"] = (
                render_paired_packet_block(packet, block)
            )
    files[
        VENUE / "BoundaryCanonicalBumpTransformPacketCorrection0PlusCachedCell0.lean"
    ] = render_cached_cell0_gate()
    files[
        VENUE / "BoundaryCanonicalBumpPairedPacketCorrection0PlusCell00.lean"
    ] = render_paired_correction0_plus_cell00()
    for start in range(0, 64, POINT_CACHE_SHARD_SIZE):
        stop = min(start + POINT_CACHE_SHARD_SIZE, 64)
        files[VENUE / f"{point_cache_stem(start, stop)}.lean"] = (
            render_point_cache_shard(start, stop)
        )
    files[VENUE / "BoundaryCanonicalBumpPointCaches.lean"] = (
        render_point_cache_aggregate()
    )
    return files


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--check",
        action="store_true",
        help="fail if generated packet modules differ from the checked-in files",
    )
    args = parser.parse_args()

    files = generated_files()
    inventory_errors: list[str] = []
    if args.check:
        inventory_errors, _ = generated_inventory_errors(
            {path.relative_to(VENUE).as_posix() for path in files},
            VENUE,
            GENERATED_PATTERNS,
        )

    stale: list[Path] = []
    for path, content in files.items():
        if args.check:
            if not path.exists() or path.read_text() != content:
                stale.append(path.relative_to(ROOT))
        else:
            path.write_text(content)
            print(f"wrote {path.relative_to(ROOT)}")

    if stale:
        for path in stale:
            print(f"stale generated file: {path}", file=sys.stderr)
    if inventory_errors:
        for error in inventory_errors:
            print(f"generated-family drift: {error}", file=sys.stderr)
    if stale or inventory_errors:
        raise SystemExit(1)


if __name__ == "__main__":
    main()
