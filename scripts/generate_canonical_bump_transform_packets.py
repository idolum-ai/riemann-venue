def rounded_rectangle(value: complex) -> tuple[Fraction, Fraction, Fraction, Fraction]:
    re_center, re_radius = rounded_interval(value.real)
    im_center, im_radius = rounded_interval(value.imag)
    return re_center, re_radius, im_center, im_radius


RationalIntervalData = tuple[Fraction, Fraction]
RationalRectangleData = tuple[RationalIntervalData, RationalIntervalData]


def interval_add(
    left: RationalIntervalData, right: RationalIntervalData
) -> RationalIntervalData:
    return left[0] + right[0], left[1] + right[1]


def interval_neg(value: RationalIntervalData) -> RationalIntervalData:
    return -value[0], value[1]


def interval_sub(
    left: RationalIntervalData, right: RationalIntervalData
) -> RationalIntervalData:
    return interval_add(left, interval_neg(right))


def interval_mul(
    left: RationalIntervalData, right: RationalIntervalData
) -> RationalIntervalData:
    left_center, left_radius = left
    right_center, right_radius = right
    return (
        left_center * right_center,
        abs(left_center) * right_radius
        + abs(right_center) * left_radius
        + left_radius * right_radius,
    )


def rectangle_add(
    left: RationalRectangleData, right: RationalRectangleData
) -> RationalRectangleData:
    return interval_add(left[0], right[0]), interval_add(left[1], right[1])


def rectangle_mul(
    left: RationalRectangleData, right: RationalRectangleData
) -> RationalRectangleData:
    return (
        interval_sub(interval_mul(left[0], right[0]), interval_mul(left[1], right[1])),
        interval_add(interval_mul(left[0], right[1]), interval_mul(left[1], right[0])),
    )


def rectangle_pow(value: RationalRectangleData, exponent: int) -> RationalRectangleData:
    result: RationalRectangleData = ((Fraction(1), Fraction(0)), (Fraction(0), Fraction(0)))
    for _ in range(exponent):
        result = rectangle_mul(result, value)
    return result


def rational_transform_raw_cache(
    re: Fraction,
    im: Fraction,
    kernel: RationalRectangleData,
    bump_cache: list[RationalIntervalData],
) -> list[RationalRectangleData]:
    """Mirror `rationalTransformRawJetInterval` over exact Fractions."""
    transform_lambda: RationalRectangleData = (
        (-im, Fraction(0)),
        (re, Fraction(0)),
    )
    raw_cache: list[RationalRectangleData] = []
    for order in range(12):
        result: RationalRectangleData = (
            (Fraction(0), Fraction(0)),
            (Fraction(0), Fraction(0)),
        )
        for bump_order in range(order + 1):
            real_factor: RationalRectangleData = (
                interval_mul(
                    (Fraction(math.comb(order, bump_order)), Fraction(0)),
                    bump_cache[bump_order],
                ),
                (Fraction(0), Fraction(0)),
            )
            kernel_factor = rectangle_mul(
                rectangle_pow(transform_lambda, order - bump_order), kernel
            )
            result = rectangle_add(result, rectangle_mul(real_factor, kernel_factor))
        raw_cache.append(result)
    return raw_cache


def canonical_bump_raw_jet_global_bound(
    order: int, lambda_bound: Fraction, kernel_bound: Fraction
) -> Fraction:
    """Mirror `canonicalBumpRawJetGlobalBound` over exact rationals."""
    return kernel_bound * sum(
        Fraction(math.comb(order, bump_order))
        * BUMP_GLOBAL_BOUNDS[bump_order]
        * lambda_bound ** (order - bump_order)
        for bump_order in range(order + 1)
    )


def cached_taylor_moment(
    radius: Fraction,
    raw_cache: list[RationalRectangleData],
    coordinate: int,
) -> Fraction:
    """Mirror `computedTransformCachedTaylorMomentAtRadiusQ`."""
    return sum(
        raw_cache[order][coordinate][0]
        * (radius ** (order + 1) - (-radius) ** (order + 1))
        / math.factorial(order + 1)
        for order in range(ORDER)
    )


def cached_taylor_coordinate_error(
    radius: Fraction,
    raw_cache: list[RationalRectangleData],
    coordinate: int,
    remainder_bound: Fraction,
) -> Fraction:
    """One coordinate of `canonicalBumpCachedCellErrorWithRemainderQ`."""
    return 2 * radius * (
        sum(
            raw_cache[order][coordinate][1]
            * radius**order
            / math.factorial(order)
            for order in range(ORDER)
        )
        + remainder_bound * radius**ORDER / math.factorial(ORDER)
    )


def cached_taylor_error(
    radius: Fraction,
    raw_cache: list[RationalRectangleData],
    remainder_bound: Fraction,
) -> Fraction:
    """Mirror `canonicalBumpCachedCellErrorWithRemainderQ`."""
    return sum(
        cached_taylor_coordinate_error(
            radius, raw_cache, coordinate, remainder_bound
        )
        for coordinate in range(2)
    )


def round_fraction_nearest(value: Fraction, denominator: int) -> Fraction:
    scaled = value * denominator
    if scaled >= 0:
        numerator = scaled.numerator // scaled.denominator
        if 2 * (scaled.numerator % scaled.denominator) >= scaled.denominator:
            numerator += 1
    else:
        positive = -scaled
        numerator = positive.numerator // positive.denominator
        if 2 * (positive.numerator % positive.denominator) >= positive.denominator:
            numerator += 1
        numerator = -numerator
    return Fraction(numerator, denominator)


def round_interval_outward(
    value: RationalIntervalData, denominator: int = CACHE_DENOMINATOR
) -> RationalIntervalData:
    """Compact an exact interval while retaining it with one ulp of slack."""
    center, radius = value
    rounded_center = round_fraction_nearest(center, denominator)
    required_radius = radius + abs(center - rounded_center)
    scaled_radius = required_radius * denominator
    radius_numerator = (
        scaled_radius.numerator + scaled_radius.denominator - 1
    ) // scaled_radius.denominator
    return rounded_center, Fraction(radius_numerator + 1, denominator)


def round_rectangle_outward(
    value: RationalRectangleData,
    denominator: int = CACHE_DENOMINATOR,
) -> RationalRectangleData:
    return (
        round_interval_outward(value[0], denominator),
        round_interval_outward(value[1], denominator),
    )


