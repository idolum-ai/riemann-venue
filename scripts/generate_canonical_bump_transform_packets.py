KERNEL_RECURRENCE_DENOMINATOR = 10**14
KERNEL_RECURRENCE_SEED_RADIUS = Fraction(1, 10**12)


def kernel_recurrence_stem(packet: Packet, reflected: bool) -> str:
    side = "Reflected" if reflected else "Forward"
    return f"BoundaryCanonicalBumpKernelRecurrence{packet.stem}{side}"


