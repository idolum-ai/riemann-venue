def bump_jet_polynomials(max_order: int) -> list[list[int]]:
    """Numerator polynomials for derivatives of the canonical bump."""

    def add(a: list[int], b: list[int]) -> list[int]:
        result = [0] * max(len(a), len(b))
        for i, value in enumerate(a):
            result[i] += value
        for i, value in enumerate(b):
            result[i] += value
        while len(result) > 1 and result[-1] == 0:
            result.pop()
        return result

    def mul(a: list[int], b: list[int]) -> list[int]:
        result = [0] * (len(a) + len(b) - 1)
        for i, x in enumerate(a):
            for j, y in enumerate(b):
                result[i + j] += x * y
        return result

    def derivative(a: list[int]) -> list[int]:
        return [i * a[i] for i in range(1, len(a))] or [0]

    gap = [1, 0, -1]
    gap_squared = mul(gap, gap)
    result = [[1]]
    for n in range(max_order):
        polynomial = result[-1]
        factor = [0, 4 * n - 2, 0, -4 * n]
        result.append(
            add(mul(gap_squared, derivative(polynomial)), mul(factor, polynomial))
        )
    return result


