def kernel_recurrence_data(
    packet: Packet, reflected: bool
) -> tuple[RationalRectangleData, RationalRectangleData, list[RationalRectangleData]]:
    """Produce the exact rounded recurrence table emitted to Lean."""
    sign = -1 if reflected else 1
    re = sign * packet.frequency
    im = sign * IM

    def seed(value: complex) -> RationalRectangleData:
        return (
            (
                round_fraction_nearest(
                    Fraction(value.real), KERNEL_RECURRENCE_DENOMINATOR
                ),
                KERNEL_RECURRENCE_SEED_RADIUS,
            ),
            (
                round_fraction_nearest(
                    Fraction(value.imag), KERNEL_RECURRENCE_DENOMINATOR
                ),
                KERNEL_RECURRENCE_SEED_RADIUS,
            ),
        )

    z = complex(float(re), float(im))
    initial = seed(cmath.exp(1j * z * float(Fraction(1, 128))))
    step = seed(cmath.exp(1j * z * float(Fraction(1, 64))))
    kernels = [initial]
    for _ in range(63):
        kernels.append(
            round_rectangle_outward(
                rectangle_mul(kernels[-1], step),
                KERNEL_RECURRENCE_DENOMINATOR,
            )
        )
    return initial, step, kernels


@cache
def paired_packet_cell_data(
    packet: Packet,
) -> tuple[tuple[Fraction, Fraction, Fraction], ...]:
    """Compute exact center/error triples for all cells of one packet."""
    _, _, forward_kernels = kernel_recurrence_data(packet, False)
    _, _, reflected_kernels = kernel_recurrence_data(packet, True)
    plus_remainder = canonical_bump_raw_jet_global_bound(
        ORDER, Fraction(104), Fraction(1)
    )
    minus_remainder = canonical_bump_raw_jet_global_bound(
        ORDER, Fraction(104), Fraction(11, 4)
    )
    cells: list[tuple[Fraction, Fraction, Fraction]] = []
    for index in range(64):
        q = Fraction(2 * index + 1, 128)
        bump_cache = [rounded_bump_point_interval(order, q) for order in range(ORDER)]
        plus_cache = rational_transform_raw_cache(
            packet.frequency, IM, forward_kernels[index], bump_cache
        )
        minus_cache = rational_transform_raw_cache(
            -packet.frequency, -IM, reflected_kernels[index], bump_cache
        )
        cells.append(
            (
                cached_taylor_moment(RADIUS, plus_cache, 0)
                + cached_taylor_moment(RADIUS, minus_cache, 0),
                cached_taylor_moment(RADIUS, plus_cache, 1)
                + cached_taylor_moment(RADIUS, minus_cache, 1),
                cached_taylor_error(RADIUS, plus_cache, plus_remainder)
                + cached_taylor_error(RADIUS, minus_cache, minus_remainder),
            )
        )
    return tuple(cells)


def paired_packet_data(packet: Packet) -> tuple[Fraction, Fraction, Fraction]:
    """Compute the exact aggregate center and error of one paired packet."""
    cells = paired_packet_cell_data(packet)
    return tuple(sum(cell[coordinate] for cell in cells) for coordinate in range(3))


