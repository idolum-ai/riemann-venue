#!/usr/bin/env python3
"""Generate exact-rational standard-bump quadrature certificate leaves."""

from __future__ import annotations

import argparse
import json
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]
ARTIFACT = ROOT / "artifacts" / "computed-phased-bump-quadrature.json"
PREFIX = "BoundaryComputedPhasedBumpQuadrature"

NORM_DEFS = """RationalInterval.absUpper, computedPhasedBumpQuadratureCell,
      explicitBumpPositiveWeightedJetCellInterval,
      explicitStandardBumpJetCellInterval,
      explicitBumpBoundaryJetPolynomialInterval,
      explicitBumpBoundaryJetCoefficients, explicitBumpBoundaryYInterval,
      explicitBumpGapInterval, monotoneExpInterval,
      RationalInterval.reciprocalPositive, RationalInterval.ofBounds,
      rangeReducedExpInterval, rationalExpInterval, rationalExpTaylor,
      rationalExpRemainder, RationalInterval.evalPolynomial,
      RationalInterval.lower, RationalInterval.upper, RationalInterval.mul,
      RationalInterval.add, RationalInterval.sub, RationalInterval.neg,
      RationalInterval.pow, RationalInterval.singleton, RationalInterval.one,
      RationalInterval.zero, Finset.sum_range_succ"""


def header(imports: list[str], title: str) -> list[str]:
    return [
        *(f"import RiemannVenue.Venue.{name}" for name in imports),
        "",
        f"/-! {title}. Generated by `scripts/generate_computed_phased_bump_quadrature.py`. -/",
        "namespace RiemannVenue.Venue",
        "noncomputable section",
        "",
    ]


def footer() -> list[str]:
    return ["", "end", "end RiemannVenue.Venue", ""]


def table_source(rows: list[list[str]]) -> str:
    lines = header(
        [f"{PREFIX}"],
        "Certified upper table for the weighted standard-bump quadratures",
    )
    lines.append("set_option maxRecDepth 100000 in")
    lines.append("def computedPhasedBumpCertifiedUpperQ : Fin 3 → Fin 256 → ℚ := ![")
    for row_index, row in enumerate(rows):
        lines.append("  ![")
        for i in range(0, 256, 8):
            suffix = "," if i + 8 < 256 else ""
            lines.append("    " + ", ".join(row[i : i + 8]) + suffix)
        lines.append("  ]" + ("," if row_index < 2 else ""))
    lines.append("]")
    lines.extend(footer())
    return "\n".join(lines)


def leaf_name(n: int, i: int) -> str:
    return f"computedPhasedBumpRawUpper_le_certified_{n}_{i}"


def leaf_file_name(n: int, block: int) -> str:
    return f"{PREFIX}LeavesN{n}Block{block}.lean"


def leaf_source(rows: list[list[str]], n: int, block: int) -> str:
    start = 16 * block
    stop = min(start + 16, 255)
    lines = header(
        [f"{PREFIX}Table"],
        f"Kernel-checked bump quadrature leaves for jet {n}, block {block}",
    )
    lines.extend(["set_option maxRecDepth 100000", ""])
    for i in range(start, stop):
        lines.extend(
            [
                "set_option maxHeartbeats 2000000 in",
                f"theorem {leaf_name(n, i)} :",
                "    (explicitBumpPositiveWeightedJetCellInterval 32 16 24 2",
                f"      {n} (computedPhasedBumpQuadratureCell ⟨{i}, by norm_num⟩)).absUpper ≤",
                f"        computedPhasedBumpCertifiedUpperQ ⟨{n}, by norm_num⟩ ⟨{i}, by norm_num⟩ := by",
                f"  change (explicitBumpPositiveWeightedJetCellInterval 32 16 24 2 {n}",
                f"    (computedPhasedBumpQuadratureCell ⟨{i}, by norm_num⟩)).absUpper ≤",
                f"      ({rows[n][i]} : ℚ)",
                f"  norm_num [{NORM_DEFS}]",
                "",
            ]
        )
    lines.extend(footer())
    return "\n".join(lines)


def selector_source() -> str:
    imports = [f"{PREFIX}Table"] + [
        f"{PREFIX}LeavesN{n}Block{block}"
        for n in range(3)
        for block in range(16)
    ]
    lines = header(imports, "Selectors for all certified bump quadrature leaves")
    lines.extend(["set_option maxRecDepth 100000", ""])
    lines.extend(
        [
            "theorem computedPhasedBumpRawUpper_le_certified",
            "    (n : ℕ) (hn : n ≤ 2) (i : Fin 256) (hi : i.val < 255) :",
            "    (explicitBumpPositiveWeightedJetCellInterval 32 16 24 2 n",
            "      (computedPhasedBumpQuadratureCell i)).absUpper ≤",
            "        computedPhasedBumpCertifiedUpperQ ⟨n, by omega⟩ i := by",
            "  interval_cases n",
        ]
    )
    for n in range(3):
        lines.append("  · fin_cases i")
        for i in range(256):
            if i < 255:
                lines.extend(
                    [
                        f"    case «{i}» =>",
                        f"      exact {leaf_name(n, i)}",
                    ]
                )
            else:
                lines.extend([f"    case «{i}» =>", "      norm_num at hi"])
    lines.extend(footer())
    return "\n".join(lines)


def write(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=ROOT / "RiemannVenue" / "Venue",
    )
    args = parser.parse_args()
    payload = json.loads(ARTIFACT.read_text(encoding="utf-8"))
    rows = payload["rows"]
    if len(rows) != 3 or any(len(row) != 256 for row in rows):
        raise SystemExit("quadrature artifact must contain a 3 x 256 table")

    write(args.output_dir / f"{PREFIX}Table.lean", table_source(rows))
    for n in range(3):
        for block in range(16):
            write(
                args.output_dir / leaf_file_name(n, block),
                leaf_source(rows, n, block),
            )
    write(
        args.output_dir / f"{PREFIX}Selectors.lean",
        selector_source(),
    )


if __name__ == "__main__":
    main()
