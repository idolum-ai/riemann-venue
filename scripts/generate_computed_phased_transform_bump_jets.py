#!/usr/bin/env python3
"""Generate sparse, kernel-checkable bump-jet numerator shards.

The generated Lean files deliberately avoid native evaluation.  Each sparse
polynomial is checked against the defining recurrence from the preceding
order, so later matrix-transform packets can unfold a small named polynomial
instead of the full recursive expression.
"""

from __future__ import annotations

import argparse
from collections import defaultdict
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]
VENUE = ROOT / "RiemannVenue" / "Venue"
SHARDS = ((0, 5), (6, 8), (9, 11), (12, 14))


def add(left: dict[int, int], right: dict[int, int]) -> dict[int, int]:
    result: defaultdict[int, int] = defaultdict(int, left)
    for degree, coefficient in right.items():
        result[degree] += coefficient
    return {degree: coefficient for degree, coefficient in result.items() if coefficient}


def mul(left: dict[int, int], right: dict[int, int]) -> dict[int, int]:
    result: defaultdict[int, int] = defaultdict(int)
    for left_degree, left_coefficient in left.items():
        for right_degree, right_coefficient in right.items():
            result[left_degree + right_degree] += left_coefficient * right_coefficient
    return dict(result)


def derivative(poly: dict[int, int]) -> dict[int, int]:
    return {degree - 1: degree * coefficient for degree, coefficient in poly.items() if degree}


def numerators() -> list[dict[int, int]]:
    values = [{0: 1}]
    square_gap = {0: 1, 2: -2, 4: 1}
    for order in range(14):
        factor = {1: 4 * order - 2, 3: -4 * order}
        values.append(add(mul(square_gap, derivative(values[-1])), mul(factor, values[-1])))
    return values


def lean_integer(value: int) -> str:
    return str(value) if value >= 0 else f"({value})"


def lean_polynomial(poly: dict[int, int]) -> str:
    terms = []
    for degree, coefficient in sorted(poly.items()):
        constant = f"({lean_integer(coefficient)} : ℤ[X])"
        terms.append(constant if degree == 0 else f"{constant} * X ^ {degree}")
    return " +\n      ".join(terms)


def module_name(start: int, stop: int) -> str:
    return f"BoundaryComputedPhasedTransformBumpJets{start}To{stop}"


def render_shard(start: int, stop: int, values: list[dict[int, int]]) -> str:
    if start == 0:
        import_line = "import RiemannVenue.Venue.BoundaryExplicitBumpJets"
    else:
        previous_start, previous_stop = SHARDS[SHARDS.index((start, stop)) - 1]
        import_line = (
            "import RiemannVenue.Venue."
            f"{module_name(previous_start, previous_stop)}"
        )
    lines = [
        import_line,
        "",
        "/-!",
        f"# Sparse bump-jet numerators {start} through {stop}",
        "",
        "Generated by `scripts/generate_computed_phased_transform_bump_jets.py`.",
        "Every polynomial is checked from the defining recurrence; these are",
        "matrix-transform proof shards, not numerical authority.",
        "-/",
        "",
        "namespace RiemannVenue.Venue",
        "",
        "open Polynomial",
        "",
    ]
    for order in range(start, stop + 1):
        previous = order - 1
        lines.extend(
            [
                f"noncomputable def computedTransformBumpJetNumerator{order} : ℤ[X] :=",
                f"  {lean_polynomial(values[order])}",
                "",
                f"theorem explicitStandardBumpJetNumerator_eq_{order} :",
                f"    explicitStandardBumpJetNumerator {order} =",
                f"      computedTransformBumpJetNumerator{order} := by",
            ]
        )
        if order == 0:
            lines.append(
                "  norm_num [explicitStandardBumpJetNumerator, "
                "computedTransformBumpJetNumerator0]"
            )
        else:
            lines.extend(
                [
                    f"  rw [show {order} = {previous} + 1 by norm_num,",
                    f"    explicitStandardBumpJetNumerator_succ,",
                    f"    explicitStandardBumpJetNumerator_eq_{previous}]",
                    "  simp [",
                    f"    computedTransformBumpJetNumerator{previous},",
                    f"    computedTransformBumpJetNumerator{order},",
                    "    Polynomial.derivative_add, Polynomial.derivative_mul,",
                    "    Polynomial.derivative_C, Polynomial.derivative_X_pow,",
                    "    Polynomial.derivative_one, Polynomial.derivative_natCast,",
                    "    Polynomial.derivative_intCast]",
                    "  all_goals ring",
                ]
            )
        lines.append("")
    lines.extend(["end RiemannVenue.Venue", ""])
    return "\n".join(lines)


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--output-dir", type=Path, default=VENUE)
    args = parser.parse_args()
    args.output_dir.mkdir(parents=True, exist_ok=True)
    values = numerators()
    for start, stop in SHARDS:
        path = args.output_dir / f"{module_name(start, stop)}.lean"
        path.write_text(render_shard(start, stop, values), encoding="utf-8")


if __name__ == "__main__":
    main()
