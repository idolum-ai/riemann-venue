#!/usr/bin/env python3
"""Generate the exact rational derivative-certificate specialization.

The generated Lean file stores only exact dictionary coefficients and invokes
the kernel-checked point evaluator.  Python does not prove any enclosure.
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path


def lean_rat(pair: list[int]) -> str:
    numerator, denominator = pair
    if denominator == 1:
        return str(numerator)
    return f"({numerator} : ℚ) / {denominator}"


def render(payload: dict) -> str:
    coefficients = payload["base_coefficients"]
    rows = [
        "import RiemannVenue.Venue.BoundaryComputedPhasedCellCertification",
        "import RiemannVenue.Venue.BoundaryComputedPhasedDerivativeIntervals",
        "",
        "/-!",
        "# Generated specialization of the phased derivative evaluator",
        "",
        "This file is generated by",
        "`scripts/generate_computed_phased_derivative_certificate.py`.",
        "It contains exact rational data only; every transcendental enclosure",
        "is discharged through the interval theorems imported above.",
        "-/",
        "",
        "namespace RiemannVenue.Venue",
        "",
        "open Finset",
        "open scoped BigOperators",
        "",
        "noncomputable section",
        "",
        "/-- Exact rational form of the signed 100-column synthesis. -/",
        "def computedPhasedBaseCoefficientQ : ComputedPhasedColumn → ℚ := ![",
    ]
    for index, coefficient in enumerate(coefficients):
        comma = "," if index + 1 < len(coefficients) else ""
        rows.append(f"  {lean_rat(coefficient)}{comma}")
    rows.extend(
        [
            "]",
            "",
            "set_option maxHeartbeats 1000000 in",
            "@[simp] theorem computedPhasedBaseCoefficientQ_cast",
            "    (j : ComputedPhasedColumn) :",
            "    (computedPhasedBaseCoefficientQ j : ℝ) =",
            "      computedPhasedBaseCoefficient j := by",
            "  fin_cases j <;> norm_num [computedPhasedBaseCoefficientQ,",
            "    computedPhasedBaseCoefficient]",
            "",
            "/-- Signed point interval for one derivative of the whole base test. -/",
            "def computedPhasedBaseJetPointInterval",
            "    (trigOrder trigHalvings bumpOrder bumpSplit n : ℕ) (t : ℚ) :",
            "    RationalInterval :=",
            "  RationalInterval.finSum fun j : ComputedPhasedColumn =>",
            "    RationalInterval.scale (computedPhasedBaseCoefficientQ j)",
            "      (computedPhasedAtomJetPointInterval trigOrder trigHalvings",
            "        bumpOrder bumpSplit n j t)",
            "",
            "/-- One cached five-column frequency shard.  The product indexing",
            "is exact: group `g` contains columns `5*g` through `5*g+4`. -/",
            "def computedPhasedBaseJetPointGroupInterval",
            "    (trigOrder trigHalvings bumpOrder bumpSplit n : ℕ) (t : ℚ)",
            "    (g : Fin 20) : RationalInterval :=",
            "  RationalInterval.finSum fun k : Fin 5 =>",
            "    let j : ComputedPhasedColumn := finProdFinEquiv (g, k)",
            "    RationalInterval.scale (computedPhasedBaseCoefficientQ j)",
            "      (computedPhasedAtomJetPointInterval trigOrder trigHalvings",
            "        bumpOrder bumpSplit n j t)",
            "",
            "theorem computedPhasedBaseJetPointGroupInterval_contains",
            "    {trigOrder trigHalvings bumpOrder bumpSplit n : ℕ} {t : ℚ}",
            "    (g : Fin 20) (hn : n ≤ 5)",
            "    (htrig : ∀ j : ComputedPhasedColumn,",
            "      |↑((computedPhasedFrequencyQ j *",
            "          (t + computedPhasedTranslationQ j)) /",
            "        (2 ^ trigHalvings : ℕ) : ℚ)| / trigOrder.succ ≤ (1 : ℝ) / 2)",
            "    (hbump : ∀ j : ComputedPhasedColumn,",
            "      |(((-(computedPhasedBumpCoordinateQ j t ^ 2 /",
            "          (1 - computedPhasedBumpCoordinateQ j t ^ 2)) /",
            "          bumpSplit : ℚ) : ℝ))| / bumpOrder.succ ≤ (1 : ℝ) / 2)",
            "    (hsplit : 0 < bumpSplit) :",
            "    (computedPhasedBaseJetPointGroupInterval trigOrder trigHalvings",
            "      bumpOrder bumpSplit n t g).Contains",
            "      (∑ k : Fin 5,",
            "        let j : ComputedPhasedColumn := finProdFinEquiv (g, k)",
            "        computedPhasedBaseCoefficient j *",
            "          (computedPhasedAtom j).iterDeriv n (t : ℝ)) := by",
            "  apply RationalInterval.contains_finSum",
            "  intro k",
            "  let j : ComputedPhasedColumn := finProdFinEquiv (g, k)",
            "  have hs := RationalInterval.contains_scale",
            "    (q := computedPhasedBaseCoefficientQ j)",
            "    (computedPhasedAtom_iterDeriv_mem_pointInterval hn",
            "      (htrig j) (hbump j) hsplit)",
            "  simpa only [computedPhasedBaseJetPointGroupInterval,",
            "    computedPhasedBaseCoefficientQ_cast] using hs",
            "",
            "/-- Soundness retains cancellation by summing all signed columns before",
            "any absolute value is taken. -/",
            "theorem computedPhasedBaseTest_iterDeriv_mem_pointInterval",
            "    {trigOrder trigHalvings bumpOrder bumpSplit n : ℕ} {t : ℚ}",
            "    (hn : n ≤ 5)",
            "    (htrig : ∀ j : ComputedPhasedColumn,",
            "      |↑((computedPhasedFrequencyQ j *",
            "          (t + computedPhasedTranslationQ j)) /",
            "        (2 ^ trigHalvings : ℕ) : ℚ)| / trigOrder.succ ≤ (1 : ℝ) / 2)",
            "    (hbump : ∀ j : ComputedPhasedColumn,",
            "      |(((-(computedPhasedBumpCoordinateQ j t ^ 2 /",
            "          (1 - computedPhasedBumpCoordinateQ j t ^ 2)) /",
            "          bumpSplit : ℚ) : ℝ))| / bumpOrder.succ ≤ (1 : ℝ) / 2)",
            "    (hsplit : 0 < bumpSplit) :",
            "    (computedPhasedBaseJetPointInterval trigOrder trigHalvings",
            "      bumpOrder bumpSplit n t).Contains",
            "      (computedPhasedBaseTest.iterDeriv n (t : ℝ)) := by",
            "  rw [computedPhasedBaseTest_iterDeriv_apply]",
            "  apply RationalInterval.contains_finSum",
            "  intro j",
            "  have hs := RationalInterval.contains_scale",
            "    (q := computedPhasedBaseCoefficientQ j)",
            "    (computedPhasedAtom_iterDeriv_mem_pointInterval hn",
            "      (htrig j) (hbump j) hsplit)",
            "  simpa only [computedPhasedBaseCoefficientQ_cast,",
            "    computedPhasedAtom_iterDeriv_apply] using hs",
            "",
            "/-- Exact interval formula for jets zero through two of the weighted",
            "base field used by the 270-cell Taylor packet. -/",
            "def computedPhasedWeightedJetPointInterval",
            "    (expOrder expSplit trigOrder trigHalvings bumpOrder bumpSplit n : ℕ)",
            "    (t : ℚ) : RationalInterval :=",
            "  let E := rangeReducedExpInterval expOrder (t / 2) expSplit",
            "  let B (k : ℕ) := computedPhasedBaseJetPointInterval trigOrder",
            "    trigHalvings bumpOrder bumpSplit k t",
            "  match n with",
            "  | 0 => RationalInterval.mul E (B 2)",
            "  | 1 => RationalInterval.mul E",
            "      (RationalInterval.add (B 3) (RationalInterval.scale (1 / 2) (B 2)))",
            "  | _ => RationalInterval.mul E",
            "      (RationalInterval.add (RationalInterval.add (B 4) (B 3))",
            "        (RationalInterval.scale (1 / 4) (B 2)))",
            "",
            "/-- Kernel-checked point evaluator for the three generated center jets. -/",
            "theorem computedPhasedWeightedJet_mem_pointInterval",
            "    {expOrder expSplit trigOrder trigHalvings bumpOrder bumpSplit n : ℕ}",
            "    {t : ℚ} (hn : n ≤ 2)",
            "    (hexpSplit : 0 < expSplit)",
            "    (hexp : |↑((t / 2 / expSplit : ℚ))| / expOrder.succ ≤ (1 : ℝ) / 2)",
            "    (htrig : ∀ j : ComputedPhasedColumn,",
            "      |↑((computedPhasedFrequencyQ j *",
            "          (t + computedPhasedTranslationQ j)) /",
            "        (2 ^ trigHalvings : ℕ) : ℚ)| / trigOrder.succ ≤ (1 : ℝ) / 2)",
            "    (hbump : ∀ j : ComputedPhasedColumn,",
            "      |(((-(computedPhasedBumpCoordinateQ j t ^ 2 /",
            "          (1 - computedPhasedBumpCoordinateQ j t ^ 2)) /",
            "          bumpSplit : ℚ) : ℝ))| / bumpOrder.succ ≤ (1 : ℝ) / 2)",
            "    (hbumpSplit : 0 < bumpSplit) :",
            "    (computedPhasedWeightedJetPointInterval expOrder expSplit trigOrder",
            "      trigHalvings bumpOrder bumpSplit n t).Contains",
            "      (iteratedDeriv n computedPhasedBaseWeightedSecond (t : ℝ)) := by",
            "  have hE := real_exp_mem_rangeReducedExpInterval",
            "    (n := expOrder) (k := expSplit) (x := t / 2) hexpSplit hexp",
            "  have hE' : (rangeReducedExpInterval expOrder (t / 2) expSplit).Contains",
            "      (Real.exp ((t : ℝ) / 2)) := by",
            "    convert hE using 1 <;> norm_num",
            "  have h2 := computedPhasedBaseTest_iterDeriv_mem_pointInterval",
            "    (n := 2) (t := t) (by norm_num) htrig hbump hbumpSplit",
            "  have h3 := computedPhasedBaseTest_iterDeriv_mem_pointInterval",
            "    (n := 3) (t := t) (by norm_num) htrig hbump hbumpSplit",
            "  have h4 := computedPhasedBaseTest_iterDeriv_mem_pointInterval",
            "    (n := 4) (t := t) (by norm_num) htrig hbump hbumpSplit",
            "  have h2half : (RationalInterval.scale (1 / 2 : ℚ)",
            "      (computedPhasedBaseJetPointInterval trigOrder trigHalvings",
            "        bumpOrder bumpSplit 2 t)).Contains",
            "      ((1 / 2 : ℝ) * computedPhasedBaseTest.iterDeriv 2 (t : ℝ)) := by",
            "    convert RationalInterval.contains_scale (q := (1 / 2 : ℚ)) h2 using 1 <;>",
            "      norm_num",
            "  have h2quarter : (RationalInterval.scale (1 / 4 : ℚ)",
            "      (computedPhasedBaseJetPointInterval trigOrder trigHalvings",
            "        bumpOrder bumpSplit 2 t)).Contains",
            "      ((1 / 4 : ℝ) * computedPhasedBaseTest.iterDeriv 2 (t : ℝ)) := by",
            "    convert RationalInterval.contains_scale (q := (1 / 4 : ℚ)) h2 using 1 <;>",
            "      norm_num",
            "  interval_cases n",
            "  · rw [iteratedDeriv_zero_computedPhasedBaseWeightedSecond]",
            "    exact RationalInterval.contains_mul hE' h2",
            "  · rw [iteratedDeriv_one_computedPhasedBaseWeightedSecond]",
            "    exact RationalInterval.contains_mul hE'",
            "      (RationalInterval.contains_add h3 h2half)",
            "  · rw [iteratedDeriv_two_computedPhasedBaseWeightedSecond]",
            "    exact RationalInterval.contains_mul hE'",
            "      (RationalInterval.contains_add (RationalInterval.contains_add h4 h3)",
            "        h2quarter)",
            "",
            "end",
            "",
            "end RiemannVenue.Venue",
            "",
        ]
    )
    return "\n".join(rows)


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--candidate",
        type=Path,
        default=Path("artifacts/localized-phased-candidate.json"),
    )
    parser.add_argument(
        "--output",
        type=Path,
        default=Path(
            "RiemannVenue/Venue/BoundaryComputedPhasedDerivativeCertificate.lean"
        ),
    )
    args = parser.parse_args()
    payload = json.loads(args.candidate.read_text())
    if payload.get("proof_authority") is not False:
        raise SystemExit("candidate JSON must remain non-authoritative")
    args.output.write_text(render(payload))
    print(f"wrote {args.output}")


if __name__ == "__main__":
    main()
